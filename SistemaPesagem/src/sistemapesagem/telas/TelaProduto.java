    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if((jtfDescricao.getText()!= null) &&(jtfQuantidade.getText() != null) && (jtfNome.getText()!= null)){
            
            Produto p = new Produto();
            p.setDescricao(jtfDescricao.getText());
            p.setNome(jtfNome.getText());
            p.setQuantidade(Integer.parseInt(jtfQuantidade.getText()));
            
            ProdutoJpaController pc = new ProdutoJpaController();
            
            pc.create(p);
            
        
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

