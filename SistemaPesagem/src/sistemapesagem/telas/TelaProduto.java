    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

            Produto p = new Produto();
            p.setId(Integer.parseInt(jtfCodigo.getText()));
            p.setDescricao(jtfDescricao.getText());
            p.setNome(jtfNome.getText());
            p.setQuantidade(Integer.parseInt(jtfQuantidade.getText()));
            
            ProdutoJpaController pc = new ProdutoJpaController();
            
        try {
            pc.edit(p);
        } catch (Exception ex) {
            Logger.getLogger(TelaProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_btnAlterarActionPerformed

